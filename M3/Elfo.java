
public class Elfo {
	private int health;
	private int strength;
	private int defense;
	private int agility;
	private int speed;
	public Elfo(int health, int strength, int defense, int agility, int speed) {
		super();
		this.health = health;
		this.strength = strength;
		this.defense = defense;
		this.agility = agility;
		this.speed = speed;
	}
	
	
}
